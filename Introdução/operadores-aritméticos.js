// +  : so,a
// -  : subtração
// *  : multiplicação
// /  : divisão
// %  : resto
// ** : potencia

var n1 = 10;
var n2 = 5;

console.log('soma n1 + n2:', n1 + n2);
console.log('subtração n1 - n2:', n1 - n2);
console.log('multiplicação n1 * n2:', n1 * n2);
console.log('divisão n1 / n2:', n1 / n2);
console.log('resto n1 % n2:', n1 % n2);
console.log('dois ao cubo 2 ** 3 :', 2 ** 3);
console.log('três ao quadrado 3 ** 2 :', 3 ** 2);
